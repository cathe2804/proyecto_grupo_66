public class Persona {
    
    protected String cedula;
    protected String nombre;

    public void setCedula(String cedula) {
        this.cedula = cedula;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public Strin getCedula() {
        return this.cedula;
    }

    public Strin getNombre() {
        return this.nombre;
    }
}
